import { HttpHandler, HttpRequest, HttpResponseInit, InvocationContext } from "@azure/functions";

export const test: HttpHandler = async function(request: HttpRequest, context: InvocationContext): Promise<HttpResponseInit> {
    context.log(`Http function processed request for url "${request.url}"`);

    const name = request.query.get('name') || await request.text() || 'world';

    return { body: `Hello, ${name}!` };
};

